def seed_data(db: Session) -> None:
    if db.scalar(select(Category).limit(1)):
        return
    categories = [Category(name=name, description=description) for name, description in CATEGORIES]
    db.add_all(categories)
    db.flush()
    db.add_all([
        Product(name=n, sku=s, category_id=c, description=d, base_type=b, power_watts=p,
                color_temperature=t, voltage=v, price=price, stock_quantity=q, image_url=img, is_active=True)
        for n, s, c, d, b, p, t, v, price, q, img in PRODUCTS
    ])
    db.commit()
